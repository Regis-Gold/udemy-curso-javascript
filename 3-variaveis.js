// let para criar váriaveis
// Não podemos criar variáveis com palavras reservadas da linguagem JS.
// Variáveis precisam ter nomes significativos
// Não pode começar o nome da váriavel com número
// Não podem conter espaçoes ou traços
// Utilizamos camelCase
// Essas variáveis são Case-sensitive
// Não podemos redeclarar variáveis com let
// Não utilizar var, utilizar let

let nome; // Declarar variável
nome = 'Reginaldo'; // Inicializando variável
console.log(nome);
nome = 'Luiz'
console.log(nome);


