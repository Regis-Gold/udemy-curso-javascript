// Operadores de comparação

let a = 100;
let b = 50;

console.log(a > b); // > maior que
console.log(a >= b); // >= maior que ou igual a
console.log(a < b); // < menor que
console.log(a <= b); // <= menor que ou igual a
console.log(a === b); // === igualdade estrita (valor e tipo)
console.log(a !== b); /* !== diferente estrito (valor e tipo) */

